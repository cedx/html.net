namespace Belin.Html.Cmdlets;

/// <summary>
/// Defines the directionality of an element's text.
/// </summary>
public enum Directionality {

	/// <summary>
	/// The user agent decides.
	/// </summary>
	Auto,

	/// <summary>
	/// From left to right.
	/// </summary>
	Ltr,

	/// <summary>
	/// From right to left.
	/// </summary>
	Rtl
}
